
public class Switch {

	public static void main(String[] args) {
		int a = 2;
		switch(a) {
		case 1:
			System.out.println("Broj nije dva");
			break;
		
		case 2:
			System.out.println("Broj je jedan");
			break;
			
		case 3:
			System.out.println("Ne pokaži ništa");
			break;
			
		default:
			System.out.println("I suck");
			break;
		}
		 
		
	}

}
