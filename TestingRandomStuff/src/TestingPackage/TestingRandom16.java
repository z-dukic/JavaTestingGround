public class TestingRandom16 {

	public static void main(String[] args) {

		int broj = 5;

		if (broj == 0) {

		} else if (broj == 1) {

		} else if (broj == 2) {

		} else if (broj == 3) {

		} else {
			System.out.println("Broj nije prepoznat");
		}

		switch (broj) {
		case 1:
			System.out.println("Tvoj broj je jedan");
			break;

		case 2:
			System.out.println("Tvoj broj je dva");
			break;
			
		case 3:
			System.out.println("Tvoj broj je tri");
			break;
			default:
				System.out.println("Broj nije nijedan od navedenih");
		}

	}

}
